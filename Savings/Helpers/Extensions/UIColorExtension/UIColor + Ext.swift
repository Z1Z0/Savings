import Foundation
import UIKit

extension UIColor {
    
    class func customCyberGrape() -> UIColor {
        return #colorLiteral(red: 0.2784313725, green: 0.1529411765, blue: 0.4666666667, alpha: 1)
    }
    
    class func customLightPurple() -> UIColor {
        return #colorLiteral(red: 0.9764705882, green: 0.5098039216, blue: 0.7647058824, alpha: 1)
    }
    
    class func customDarkPurple() -> UIColor {
        return #colorLiteral(red: 0.9450980392, green: 0.3725490196, blue: 0.6784313725, alpha: 1)
    }
}
